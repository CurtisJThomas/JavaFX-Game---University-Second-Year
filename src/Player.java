
public class Player {
//Player class to store users name and score.
		private String name;
		private String score;
		
		//Sets the name and score.
		public Player(String name, String score){
			this.name = name;
			this.score = score;
		}
		//Returns the name
		public String getName() {
			return name;
		}

		//Returns the score
		public String getScore() {
			return score;
		}

	}


